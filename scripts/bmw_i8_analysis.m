# BMW i8 Vehicle Performance Analysis (Octave)

This project analyzes the longitudinal performance of the BMW i8 using
engineering-based vehicle dynamics calculations implemented in GNU Octave.

## Objective
To evaluate acceleration capability and resistive forces acting on the vehicle,
including tractive force, aerodynamic drag, rolling resistance, and grade resistance.

## Engineering Methods
- Calculated tractive force from engine torque, gear ratios, and drivetrain efficiency
- Modeled aerodynamic drag using Cd, frontal area, and air density
- Modeled rolling resistance as a function of vehicle speed and tire parameters
- Evaluated grade resistance for different road inclinations
- Generated forceâ€“speed and acceleration plots for performance evaluation

## Key Assumptions
- Vehicle parameters based on published BMW i8 specifications
- Constant drivetrain efficiency
- Steady-state longitudinal motion

## Results
- Tractive force exceeds resistive forces at low and medium speeds
- Aerodynamic drag becomes dominant at higher speeds
- Road gradient significantly reduces available acceleration

